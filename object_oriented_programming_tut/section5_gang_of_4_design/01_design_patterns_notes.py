# Gang of Four (GoF) Design Patterns:
# The term "Gang of Four" refers to the four authors of the influential book "Design Patterns: Elements of Reusable Object-Oriented Software":
# Erich Gamma, Richard Helm, Ralph Johnson, and John Vlissides.
# This book cataloged 23 design patterns, which are now widely recognized and used in software development.

# The GoF patterns are categorized into three main types:

# 1. Creational Patterns:
#    These patterns deal with object creation mechanisms, trying to create objects in a manner suitable to the situation.
#    Examples: Factory Method, Abstract Factory, Singleton, Builder, Prototype.

# 2. Structural Patterns:
#    These patterns deal with the composition of classes and objects. They describe how classes and objects can be combined to form larger structures.
#    Examples: Adapter, Bridge, Composite, Decorator, Facade, Flyweight, Proxy.
#    - Facade Pattern
#    - Adapter Pattern

# 3. Behavioral Patterns:
#    These patterns deal with algorithms and the assignment of responsibilities between objects. They describe how objects interact and distribute responsibilities.
#    Examples: Chain of Responsibility, Command, Interpreter, Iterator, Mediator, Memento, Observer, State, Strategy, Template Method, Visitor.
#    - State Pattern
#    - Observer Patter

# Understanding and applying these patterns can lead to more flexible, maintainable, and reusable code.


